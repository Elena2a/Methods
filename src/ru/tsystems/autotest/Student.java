package ru.tsystems.autotest;

public class Student {
        private String name;
        private int roll_no;

        public Student(String name, int roll_no) {
            this.name = name;
            this.roll_no = roll_no;
        }

        public String getName() {
            return name;
        }

        public int getRollNo() {
            return roll_no;
        }
    }


